/** Build the demo custom dish for a meal: SCALED to the patient's stated
 *  numbers (a "200 calorie" request yields a ≈200 kcal dish, never 380),
 *  then folding in a requested drink with honest macro adjustments. */
function demoCustom(
  meal: Moment,
  drink: ParsedWants['drink'],
  kcal: number | null,
  gramsTarget: number | null
): GeneratedDish {
  let base = DEMO_BASE[meal] ?? DEMO_BASE.ghda;
  // The patient's numbers are HARD constraints → scale the whole dish.
  const f = kcal ? kcal / base.calories : gramsTarget ? gramsTarget / base.grams : 1;
  if (f !== 1) {
    const fc = Math.min(2.5, Math.max(0.25, f));
    const grams = Math.round(base.grams * fc);
    base = {
      ...base,
      grams,
      serving: /\(\d+\s*g\)/.test(base.serving)
        ? base.serving.replace(/\(\d+\s*g\)/, `(${grams} g)`)
        : `${grams} g`,
      calories: kcal ?? Math.round(base.calories * fc),
      carbs: Math.round(base.carbs * fc),
      sugar: Math.round(base.sugar * fc),
      protein: Math.round(base.protein * fc),
      fat: Math.round(base.fat * fc),
      fiber: Math.round(base.fiber * fc),
      // Gram quantities inside the ingredient lines shrink/grow too, so
      // the recipe stays coherent with the scaled serving.
      ingredients: base.ingredients.map((ing) =>
        ing.replace(/(\d+)\s*g\b/g, (_full, n) => `${Math.max(5, Math.round((parseInt(n, 10) * fc) / 5) * 5)} g`)
      ),
    };
  }
  if (!drink || meal === 'drink') return { ...base };
  const d: GeneratedDish = {
    ...base,
    name: `${base.name} + ${drink.labelFr}`,
    serving: `${base.serving} + 1 verre de ${drink.labelFr}`,
    ingredients: [...base.ingredients, `1 verre de ${drink.labelFr} frais (sans sucre ajouté)`],
    steps: [...base.steps, `Accompagnez d'un verre de ${drink.labelFr} sans sucre ajouté.`],
  };
  if (drink.sugary) {
    d.calories = base.calories + 50;
    d.carbs = base.carbs + 12;
    d.sugar = base.sugar + 10;
    d.why = `${base.why} ⚠️ Le ${drink.labelFr} est naturellement sucré : un petit verre (150 ml), de préférence pendant le repas.`;
  }
  return d;
}

/**
 * Mirror the patient (demo edition): pick the reply language from their LAST
 * message — Arabic script or Latin-Darija markers → Arabic, French/German/
 * English keywords → that language, otherwise the app language. The real AI
 * does this natively (any language, replying in Latin Darija too).
 */
function detectReplyLang(msg: string, fallback: string): string {
  const s = (msg || '').replace(/^🎙️\s*/, '');
  if (/[؀-ۿ]/.test(s)) return 'ar';
  if (/\b(bghit|wach|chno|ch7al|khass|zid|dial|dyal|liya|3andi|3endi|matnsach|nakou?l|makla|mzyan|sa7a|daba|hadchi|3assir|3afak|walou|safi)\b/i.test(s)) return 'ar';
  if (/\b(je|une?|avec|pour|manger|repas|l[ée]ger|d[ée]jeuner|d[îi]ner|envie|surprenez)\b/i.test(s)) return 'fr';
  if (/\b(ich|eine?n?|mit|f[üu]r|essen|leicht|fr[üu]hst[üu]ck|abendessen|mittagessen|m[öo]chte|[üu]berrasch)\b/i.test(s)) return 'de';
  if (/\b(i|want|with|for|eat|light|breakfast|lunch|dinner|meal|surprise)\b/i.test(s)) return 'en';
  return fallback;
}

function demoResult(
  messages: { role: 'user' | 'assistant'; content: string }[],
  language: string,
  generate: boolean
): CoachResult {
  const userMsgs = messages.filter((m) => m.role === 'user').map((m) => m.content);
  // Speak the language the patient is actually using RIGHT NOW.
  const lang = detectReplyLang(userMsgs[userMsgs.length - 1] ?? '', language);
  const tx = DEMO_TXT[lang] ?? DEMO_TXT.fr;
  const convo = userMsgs.join('\n');
  const meal = detectMeal(convo);
  const parsed = parseWants(userMsgs, lang);

  if (generate) {
    // Keep the meal chosen earlier — never drift to another one.
    const m: Moment = meal ?? 'ghda';

    // Daily objective: what's left of today's reference budget. The
    // patient's explicit calorie wish always wins; otherwise, when the
    // remaining budget is smaller than the base dish, shrink to fit it.
    const s = useAppStore.getState();
    const isToday = (iso: string) =>
      new Date(iso).toDateString() === new Date().toDateString();
    const eatenKcal = Math.round(
      s.meals.filter((x) => isToday(x.created_at)).reduce((a, x) => a + x.result.calories, 0)
    );
    const eatenCarbs = Math.round(
      s.meals.filter((x) => isToday(x.created_at)).reduce((a, x) => a + x.result.carbohydrates, 0)
    );
    const remKcal = Math.max(0, 2000 - eatenKcal);
    const remCarbs = Math.max(0, 250 - eatenCarbs);
    let kcalTarget = parsed.kcal;
    let objNote = '';
    if (!kcalTarget && eatenKcal > 0) {
      objNote = (OBJ_TXT[lang] ?? OBJ_TXT.fr)(remKcal, remCarbs);
      // Fill the remaining budget: the dish targets the WHOLE remainder
      // ("500 left" → a ≈500 kcal proposal, never a tiny 220 one), capped
      // at a sensible ceiling for this meal type.
      if (remKcal >= 120) {
        kcalTarget = Math.min(remKcal, MEAL_KCAL_CAP[m] ?? 700);
      }
    }

    const custom = demoCustom(m, parsed.drink, kcalTarget, parsed.grams);
    const cid = registerCustomDish(buildCustomHealthyFood(custom, ''));
    // AI-created dish FIRST, then ready dishes from the SAME meal folder
    // (the chat renders them as two labelled groups). With a calorie target,
    // pick the catalog dishes CLOSEST to it.
    const dishes: DishCard[] = [{ kind: 'custom', id: cid, note: '', dish: custom }];
    let cat = filterHealthyFoods('', m);
    if (kcalTarget) {
      cat = cat
        .slice()
        .sort((a, b) => Math.abs(a.calories - kcalTarget!) - Math.abs(b.calories - kcalTarget!));
    }
    for (const f of cat.slice(0, 2)) {
      dishes.push({ kind: 'catalog', id: f.id, note: '' });
    }
    return {
      reply: objNote ? `${objNote}\n\n${tx.result}` : tx.result,
      quickReplies: [],
      recap: null,
      dishes: dishes.slice(0, 3),
      transcript: '',
    };
  }

  // Guided flow: 1. learn the meal → 2. ask preferences/allergies ONCE →
  // 3. understood recap (accumulated across edits).
  if (!meal) {
    return { reply: tx.ask, quickReplies: tx.meals, recap: null, dishes: null, transcript: '' };
  }
  const askedPref = messages.some(
    (m) => m.role === 'assistant' && ALL_ASK2.some((a) => m.content.includes(a))
  );
  if (!askedPref) {
    return { reply: tx.ask2, quickReplies: tx.quick2, recap: null, dishes: null, transcript: '' };
  }

  const label = mealLabel(meal, lang);
  const withoutWord = ({ fr: 'sans', en: 'without', de: 'ohne', ar: 'بلا' } as Record<string, string>)[lang] ?? 'sans';
  // "Frühstück — leicht · Bananensaft" reads naturally in every language
  // (a "with" word clashes with adjective tags like léger/leicht).
  const wantBits = [...parsed.wants, ...(parsed.drink ? [parsed.drink.label] : [])];
  let summary = label;
  if (wantBits.length) summary += ` — ${wantBits.join(' · ')}`;
  if (parsed.avoid.length) summary += ` · ${withoutWord} ${parsed.avoid.join(', ')}`;
  // The coach proves it knows the patient's day (glucose / carbs / sport).
  const note = demoHealthNote(lang);
  return {
    reply: note ? `${note}\n\n${tx.recap}` : tx.recap,
    quickReplies: [],
    recap: { title: label, meal, summary, wants: wantBits.slice(0, 4), avoid: parsed.avoid },
    dishes: null,
    transcript: '',
  };
}

/**
 * Ask the healthy-meal coach. Normal turns gather info / return a recap;
 * pass `generate: true` (the "Générer" button) to get the dish cards.
 * Personalized with the patient's health snapshot and the curated catalog.
 */
export async function healthyCoach(
  messages: { role: 'user' | 'assistant'; content: string }[],
  language: string,
  profile: Profile | null,
  opts: {
    generate?: boolean;
    audio?: { mimeType: string; data: string };
    /** The meal moment the recap settled on — dishes are filtered to it. */
    meal?: Moment | 'any' | null;
  } = {}
): Promise<CoachResult> {
  const generate = !!opts.generate;

  if (isDemoMode || !supabase) {
    await new Promise((r) => setTimeout(r, generate ? 900 : 600));
    return demoResult(messages, language, generate);
  }

  // On "Générer", end the conversation on a clear user intent so Gemini
  // produces the dishes for this turn (the `generate` flag drives the mode).
  const turns = generate
    ? [...messages, { role: 'user' as const, content: 'Génère maintenant les plats.' }]
    : messages;

  const { data, error } = await supabase.functions.invoke('ai-chat', {
    body: {
      messages: turns,
      language,
      profile,
      mode: 'healthy_coach',
      generate,
      meal: opts.meal ?? null,
      healthData: buildHealthContext(),
      // Full chronological journal (today + yesterday: every meal with its
      // details, every dose, every measure, sport) — same depth the insulin
      // engine gets, so the coach truly knows the patient's day.
      dayJournal: buildAIDayJournal(),
      catalog: healthyFoodAIIndex(),
      ...(opts.audio ? { audio: opts.audio } : {}),
    },
  });
  const quota = await asQuotaError(error, data);
  if (quota) throw quota;
  if (error) throw error;
  if (data?.error) throw new Error(data.error);

  const r = (data?.result ?? {}) as Record<string, unknown>;
  return {
    reply: str(r.reply),
    quickReplies: strArr(r.quickReplies).slice(0, 4),
    recap: parseRecap(r.recap),
    dishes: parseDishes(r.dishes, opts.meal),
    transcript: str(r.transcript),
  };
}
