/* ────────────────────────────────────────────────────────────
 * HEALTHY MEAL COACH — the "Makla saine" guided assistant.
 *
 * A conversational flow that gathers what the patient wants (which meal,
 * which ingredients, dislikes/allergies), shows a RECAP the patient can
 * confirm or edit, and on "Générer" proposes several dishes: ready ones
 * from our curated catalog PLUS a fully CUSTOM dish built to order (same
 * grams the patient asked for) when nothing in the list matches exactly.
 *
 * Custom dishes are generated by the `ai-chat` edge function (mode
 * `healthy_coach`) and rendered on the SAME detail screen as catalog
 * dishes — we shape them into a `HealthyFood` and keep them in an
 * in-session registry so `/healthy-food?custom=<id>` can resolve them.
 * ──────────────────────────────────────────────────────────── */

/** The confirmation the patient reviews before dishes are generated. */
export interface Recap {
  /** Short human title, e.g. "Déjeuner — salade fraîche". */
  title: string;
  /** Meal-time folder the request is about. */
  meal: Moment | 'any';
  /** One-line human recap of what the patient asked for. */
  summary: string;
  /** Ingredients / attributes they want. */
  wants: string[];
  /** Dislikes / allergies to avoid. */
  avoid: string[];
}

/** A dish the AI invents on the spot from the patient's request. Already
 *  localized in the patient's language (generation is per-language). */
export interface GeneratedDish {
  name: string;
  emoji: string;
  category: HealthyCategory;
  /** Human serving label, e.g. "1 assiette (250 g)". */
  serving: string;
  grams: number;
  calories: number;
  carbs: number;
  sugar: number;
  protein: number;
  fat: number;
  fiber: number;
  gi: number;
  why: string;
  ingredients: string[];
  steps: string[];
}

/** A tappable dish card in the chat — a ready catalog dish, or a custom one. */
export type DishCard =
  | { kind: 'catalog'; id: string; note?: string }
  | { kind: 'custom'; id: string; note?: string; dish: GeneratedDish };

export interface CoachResult {
  reply: string;
  /** Ready-to-tap follow-up chips (e.g. meal choices). */
  quickReplies: string[];
  /** Set once the AI knows enough → the app shows the confirm card. */
  recap: Recap | null;
  /** Set on "Générer" → the app shows the dish cards. */
  dishes: DishCard[] | null;
  /** Faithful transcript when the turn was a voice message. */
  transcript: string;
}

/* ── In-session registry of generated dishes ──
 * Kept in a module Map (not the persisted store): generated dishes are
 * ephemeral, and this survives in-session navigation to the detail page.
 * A stale id (after a full web reload) simply redirects home. */
const customRegistry = new Map<string, HealthyFood>();
let customSeq = 0;

const VALID_CATEGORIES: HealthyCategory[] = [
  'breakfast', 'salad', 'soup', 'main', 'seafood', 'snack', 'drink', 'dessert',
];

const CATEGORY_EMOJI: Record<HealthyCategory, string> = {
  breakfast: '🌅', salad: '🥗', soup: '🍲', main: '🍽️',
  seafood: '🐟', snack: '🥜', drink: '🍵', dessert: '🍓',
};

/** Which meal-time folders a custom dish belongs to (for the detail hero). */
function momentsForCategory(cat: HealthyCategory): Moment[] {
  switch (cat) {
    case 'breakfast': return ['ftour'];
    case 'snack': return ['snack'];
    case 'drink': return ['drink'];
    case 'dessert': return ['dessert'];
    default: return ['ghda', '3cha'];
  }
}

/**
 * Turn a generated dish into a `HealthyFood` so the existing detail screen
 * and its helpers (`healthyFoodName/Why/Ingredients/Steps`) render it with
 * zero changes. The already-localized text goes into every language field.
 */
export function buildCustomHealthyFood(d: GeneratedDish, id: string): HealthyFood {
  const category: HealthyCategory = VALID_CATEGORIES.includes(d.category)
    ? d.category
    : 'main';
  const emoji = d.emoji || CATEGORY_EMOJI[category];
  const grams = Number(d.grams) > 0 ? Math.round(Number(d.grams)) : 0;
  const num = (v: unknown) => (Number.isFinite(Number(v)) ? Math.round(Number(v)) : 0);
  return {
    id,
    name_fr: d.name, name_ar: d.name, name_en: d.name,
    category,
    moments: momentsForCategory(category),
    emoji,
    serving: d.serving || (grams ? `${grams} g` : ''),
    grams,
    calories: num(d.calories),
    carbs: num(d.carbs),
    sugar: num(d.sugar),
    protein: num(d.protein),
    fat: num(d.fat),
    fiber: num(d.fiber),
    gi: num(d.gi),
    why_fr: d.why, why_ar: d.why,
    ingredients_fr: Array.isArray(d.ingredients) ? d.ingredients : [],
    ingredients_ar: Array.isArray(d.ingredients) ? d.ingredients : [],
    steps_fr: Array.isArray(d.steps) ? d.steps : [],
    steps_ar: Array.isArray(d.steps) ? d.steps : [],
  };
}

/** Register a generated dish for in-session lookup; returns its id. */
export function registerCustomDish(food: HealthyFood): string {
  const id = food.id || `custom-${Date.now().toString(36)}-${(customSeq++).toString(36)}`;
  customRegistry.set(id, { ...food, id });
  return id;
}

/** Resolve a generated dish by id (used by the detail screen). */
export function getCustomDish(id: string): HealthyFood | null {
  return customRegistry.get(id) ?? null;
}

/* ── Response parsing helpers ── */
const str = (v: unknown): string => (typeof v === 'string' ? v : '');
const strArr = (v: unknown): string[] =>
  Array.isArray(v) ? v.filter((x): x is string => typeof x === 'string' && !!x.trim()) : [];

function parseRecap(v: unknown): Recap | null {
  if (!v || typeof v !== 'object') return null;
  const o = v as Record<string, unknown>;
  const summary = str(o.summary);
  if (!summary) return null;
  return {
    title: str(o.title) || summary,
    meal: (['ftour', 'ghda', '3cha', 'snack', 'drink', 'dessert'] as const).includes(
      o.meal as Moment
    )
      ? (o.meal as Moment)
      : 'any',
    summary,
    wants: strArr(o.wants),
    avoid: strArr(o.avoid),
  };
}

function parseDishes(v: unknown, meal?: Moment | 'any' | null): DishCard[] | null {
  if (!Array.isArray(v)) return null;
  const out: DishCard[] = [];
  for (const raw of v) {
    if (!raw || typeof raw !== 'object') continue;
    const o = raw as Record<string, unknown>;
    const note = str(o.note);
    const dish = o.dish as Partial<GeneratedDish> | undefined;
    // Custom dish (has a full spec) → register + shape into a HealthyFood.
    if (o.kind === 'custom' || (dish && typeof dish === 'object' && dish.name)) {
      if (!dish || !dish.name || !(Number(dish.grams) > 0)) continue;
      const built = buildCustomHealthyFood(dish as GeneratedDish, '');
      const id = registerCustomDish(built);
      out.push({ kind: 'custom', id, note, dish: dish as GeneratedDish });
    } else {
      // Catalog dish → keep only ids that exist AND belong to the requested
      // meal moment (safeguard: never show a lunch tagine for breakfast).
      const id = str(o.id);
      const food = id ? getHealthyFood(id) : null;
      if (food && (!meal || meal === 'any' || food.moments.includes(meal))) {
        out.push({ kind: 'catalog', id, note });
      }
    }
    if (out.length >= 3) break;
  }
  return out;
}

