/** One personalized sentence from the store (most important fact first). */
function demoHealthNote(language: string): string {
  const tx = HEALTH_TXT[language] ?? HEALTH_TXT.fr;
  const s = useAppStore.getState();
  const isToday = (iso: string) =>
    new Date(iso).toDateString() === new Date().toDateString();
  const glucose = s.glucoseLogs
    .filter((g) => isToday(g.created_at))
    .sort((a, b) => a.created_at.localeCompare(b.created_at));
  const last = glucose[glucose.length - 1];
  const high = s.profile?.target_high ?? 180;
  const carbs = Math.round(
    s.meals
      .filter((m) => isToday(m.created_at))
      .reduce((a, m) => a + m.result.carbohydrates, 0)
  );
  const sport = s.activityLogs
    .filter((a) => isToday(a.created_at))
    .reduce((a, x) => a + x.duration_min, 0);
  if (last && last.value > high) return tx.glucoseHigh(last.value);
  if (carbs > 150) return tx.carbsHigh(carbs);
  if (sport > 0) return tx.sport(sport);
  if (last) return tx.glucoseOk(last.value);
  return '';
}

