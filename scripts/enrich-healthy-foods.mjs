/**
 * Fills src/data/healthyFoodDetails.ts with DETAILED recipes (quantified
 * ingredients + granular steps + a diabetes tip, fr + ar) by calling the
 * `enrich-dishes` edge function (which uses the server-side GEMINI_API_KEY).
 * The human then reviews the result before deploy. Run online:
 *
 *     cd glucoai && node scripts/enrich-healthy-foods.mjs
 *     # optional: node scripts/enrich-healthy-foods.mjs --force   (redo all)
 *
 * Merges with existing details, so a re-run only fills the dishes that are
 * still missing (e.g. after a transient AI failure).
 */
import fs from 'node:fs';
import path from 'node:path';
import url from 'node:url';
import ts from 'typescript';

const here = path.dirname(url.fileURLToPath(import.meta.url));
const root = path.resolve(here, '..');
const FORCE = process.argv.includes('--force');

function env(key) {
  const m = fs.readFileSync(path.join(root, '.env'), 'utf8').match(new RegExp(`^${key}=(.*)$`, 'm'));
  return m ? m[1].trim() : '';
}
function loadModule(relPath) {
  const src = fs.readFileSync(path.join(root, relPath), 'utf8');
  const js = ts.transpileModule(src, {
    compilerOptions: { module: ts.ModuleKind.CommonJS, target: ts.ScriptTarget.ES2019 },
  }).outputText;
  const mod = { exports: {} };
  new Function('exports', 'module', 'require', js)(mod.exports, mod, () => ({}));
  return mod.exports;
}

const SUPABASE_URL = env('EXPO_PUBLIC_SUPABASE_URL');
const ANON = env('EXPO_PUBLIC_SUPABASE_ANON_KEY');
const FN_URL = `${SUPABASE_URL}/functions/v1/enrich-dishes`;

const { HEALTHY_FOODS } = loadModule('src/data/healthyFoods.ts');
const detailsPath = path.join(root, 'src/data/healthyFoodDetails.ts');
let details = {};
try {
  if (!FORCE) details = loadModule('src/data/healthyFoodDetails.ts').HEALTHY_FOOD_DETAILS ?? {};
} catch {}

const todo = HEALTHY_FOODS.filter((f) => !details[f.id]);
console.log(`${HEALTHY_FOODS.length} dishes, ${Object.keys(details).length} already enriched, ${todo.length} to do.`);

const payload = (f) => ({
  id: f.id, name_fr: f.name_fr, name_ar: f.name_ar, name_en: f.name_en,
  serving: f.serving, grams: f.grams, calories: f.calories, carbs: f.carbs,
  sugar: f.sugar, gi: f.gi, category: f.category, why_fr: f.why_fr,
});

async function callBatch(batch) {
  try {
    const r = await fetch(FN_URL, {
      method: 'POST',
      headers: { Authorization: `Bearer ${ANON}`, apikey: ANON, 'Content-Type': 'application/json' },
      body: JSON.stringify({ dishes: batch.map(payload) }),
      signal: AbortSignal.timeout(120_000),
    });
    if (!r.ok) { console.log(`  batch HTTP ${r.status}`); return []; }
    const d = await r.json();
    return Array.isArray(d.results) ? d.results : [];
  } catch (e) {
    console.log(`  batch error: ${e}`);
    return [];
  }
}

// Split into batches of 6; run 3 batches concurrently.
const BATCH = 6, POOL = 3;
const batches = [];
for (let i = 0; i < todo.length; i += BATCH) batches.push(todo.slice(i, i + BATCH));

let done = 0;
for (let i = 0; i < batches.length; i += POOL) {
  const group = batches.slice(i, i + POOL);
  const settled = await Promise.all(group.map(callBatch));
  for (const results of settled) {
    for (const res of results) {
      if (res && res.ok) {
        details[res.id] = {
          ingredients_fr: res.ingredients_fr,
          ingredients_ar: res.ingredients_ar,
          steps_fr: res.steps_fr,
          steps_ar: res.steps_ar,
        };
        done++;
      } else if (res) {
        console.log(`  ⚠ ${res.id} — AI failed (keeps hand version)`);
      }
    }
  }
  console.log(`  ...${done}/${todo.length}`);
}

// Write in dish order for a stable diff.
const body = HEALTHY_FOODS.filter((f) => details[f.id])
  .map((f) => `  ${JSON.stringify(f.id)}: ${JSON.stringify(details[f.id])},`)
  .join('\n');

const file = `/**
 * DETAILED recipes (quantified ingredients + granular steps + a diabetes tip,
 * fr + ar) for the curated healthy dishes. Generated by
 * scripts/enrich-healthy-foods.mjs (Gemini via the enrich-dishes edge fn),
 * then reviewed by hand. When an id is present here, healthyFoodIngredients /
 * healthyFoodSteps use THIS instead of the terse inline version.
 */
export interface HealthyFoodDetail {
  ingredients_fr: string[];
  ingredients_ar: string[];
  steps_fr: string[];
  steps_ar: string[];
}

export const HEALTHY_FOOD_DETAILS: Record<string, HealthyFoodDetail> = {
${body}
};
`;
fs.writeFileSync(detailsPath, file, 'utf8');
console.log(`\n✅ ${Object.keys(details).length}/${HEALTHY_FOODS.length} dishes detailed → src/data/healthyFoodDetails.ts`);
