/**
 * Generates supabase/migrations/0017_healthy_foods.sql from the single
 * source of truth src/data/healthyFoods.ts (+ image URLs from
 * healthyFoodImages.ts). Run it after editing the dishes so the Supabase
 * `healthy_foods` mirror stays in sync:
 *
 *     cd glucoai && node scripts/gen-healthy-foods-sql.mjs
 *
 * The TS files are transpiled in-memory with the TypeScript compiler and
 * evaluated (no external imports at runtime — only erased type imports),
 * so we never hand-duplicate 110 rows.
 */
import fs from 'node:fs';
import path from 'node:path';
import url from 'node:url';
import ts from 'typescript';

const here = path.dirname(url.fileURLToPath(import.meta.url));
const root = path.resolve(here, '..');

function loadModule(relPath) {
  const src = fs.readFileSync(path.join(root, relPath), 'utf8');
  const js = ts.transpileModule(src, {
    compilerOptions: { module: ts.ModuleKind.CommonJS, target: ts.ScriptTarget.ES2019 },
  }).outputText;
  const mod = { exports: {} };
  // Only type-imports exist in these files; they are erased, so require() is a no-op stub.
  new Function('exports', 'module', 'require', js)(mod.exports, mod, () => ({}));
  return mod.exports;
}

const { HEALTHY_FOODS } = loadModule('src/data/healthyFoods.ts');
const { HEALTHY_FOOD_IMAGES } = loadModule('src/data/healthyFoodImages.ts');

const q = (s) => `'${String(s ?? '').replace(/'/g, "''")}'`;
const qNull = (s) => (s == null ? 'null' : q(s));
const arr = (a) => `ARRAY[${(a ?? []).map(q).join(', ')}]::text[]`;
const jsonb = (a) => `${q(JSON.stringify(a ?? []))}::jsonb`;
const num = (n) => (n == null ? 'null' : Number(n));

const rows = HEALTHY_FOODS.map((f) => {
  const img = HEALTHY_FOOD_IMAGES[f.id] ?? null;
  return `  (${q(f.id)}, ${q(f.name_fr)}, ${q(f.name_ar)}, ${q(f.name_en)}, ${q(f.category)}, ${arr(
    f.moments
  )}, ${q(f.emoji)}, ${q(f.serving)}, ${num(f.grams)}, ${num(f.calories)}, ${num(f.carbs)}, ${num(
    f.sugar
  )}, ${num(f.protein)}, ${num(f.fat)}, ${num(f.fiber)}, ${num(f.gi)}, ${qNull(f.why_fr)}, ${qNull(
    f.why_ar
  )}, ${jsonb(f.ingredients_fr)}, ${jsonb(f.ingredients_ar)}, ${jsonb(f.steps_fr)}, ${jsonb(
    f.steps_ar
  )}, ${qNull(img)}, ${arr(f.aliases)})`;
}).join(',\n');

const sql = `-- Curated HEALTHY FOODS ("Sélection santé") — server-side mirror of
-- src/data/healthyFoods.ts. ~${HEALTHY_FOODS.length} diabetes-friendly Moroccan dishes with
-- meal-time folders (moments), ingredients and preparation, so the AI and
-- scanner read PRE-STORED values instead of recomputing.
--
-- AUTO-GENERATED by scripts/gen-healthy-foods-sql.mjs — do not edit by hand.
-- Read-only for clients (RLS select-only).

create table if not exists public.healthy_foods (
  id text primary key,
  name_fr text not null,
  name_ar text not null,
  name_en text not null,
  category text not null,
  moments text[] not null default '{}',
  emoji text not null default '🍽️',
  serving text not null,
  grams numeric not null,
  calories numeric not null,
  carbs numeric not null,
  sugar numeric not null default 0,
  protein numeric not null default 0,
  fat numeric not null default 0,
  fiber numeric not null default 0,
  gi numeric,
  why_fr text,
  why_ar text,
  ingredients_fr jsonb not null default '[]',
  ingredients_ar jsonb not null default '[]',
  steps_fr jsonb not null default '[]',
  steps_ar jsonb not null default '[]',
  image_url text,
  aliases text[] default '{}',
  created_at timestamptz not null default now()
);

alter table public.healthy_foods enable row level security;

drop policy if exists "healthy_foods_read" on public.healthy_foods;
create policy "healthy_foods_read" on public.healthy_foods
  for select using (true);

create index if not exists healthy_foods_name_fr_idx
  on public.healthy_foods using gin (to_tsvector('french', name_fr));
create index if not exists healthy_foods_moments_idx
  on public.healthy_foods using gin (moments);

insert into public.healthy_foods
  (id, name_fr, name_ar, name_en, category, moments, emoji, serving, grams,
   calories, carbs, sugar, protein, fat, fiber, gi, why_fr, why_ar,
   ingredients_fr, ingredients_ar, steps_fr, steps_ar, image_url, aliases)
values
${rows}
on conflict (id) do update set
  name_fr = excluded.name_fr,
  name_ar = excluded.name_ar,
  name_en = excluded.name_en,
  category = excluded.category,
  moments = excluded.moments,
  emoji = excluded.emoji,
  serving = excluded.serving,
  grams = excluded.grams,
  calories = excluded.calories,
  carbs = excluded.carbs,
  sugar = excluded.sugar,
  protein = excluded.protein,
  fat = excluded.fat,
  fiber = excluded.fiber,
  gi = excluded.gi,
  why_fr = excluded.why_fr,
  why_ar = excluded.why_ar,
  ingredients_fr = excluded.ingredients_fr,
  ingredients_ar = excluded.ingredients_ar,
  steps_fr = excluded.steps_fr,
  steps_ar = excluded.steps_ar,
  image_url = coalesce(excluded.image_url, public.healthy_foods.image_url),
  aliases = excluded.aliases;
`;

const outPath = path.join(root, 'supabase/migrations/0017_healthy_foods.sql');
fs.writeFileSync(outPath, sql, 'utf8');
console.log(`✅ Wrote ${HEALTHY_FOODS.length} dishes → ${path.relative(root, outPath)}`);
